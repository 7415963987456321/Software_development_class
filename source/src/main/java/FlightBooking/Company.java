package FlightBooking;

public class Company{
    private String name;
    private int id;

    // Setters and getters

    // Name
    public void setName(String companyName) {
        name = companyName;
    }

    public String getName() {
        return name;
    }

    // Id
    public void setId(int companyID) {
        id = companyID;
    }

    public int getId() {
        return id;
    }
}
